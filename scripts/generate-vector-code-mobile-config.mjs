import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(scriptDir, '..');
const fixturePath = path.join(root, 'src/vs/workbench/contrib/vectorCode/common/vectorCodeMobileProtocolFixtures.json');
const tsOutputPath = path.join(root, 'src/vs/workbench/contrib/vectorCode/common/vectorCodeGeneratedConfig.ts');
const swiftOutputPath = path.join(root, 'apps/ios/Sources/VectorCodeMobile/Generated/VectorCodeGeneratedConfig.swift');
const checkOnly = process.argv.includes('--check');

const fixture = JSON.parse(await readFile(fixturePath, 'utf8'));
const protocolVersion = fixture.protocolVersion;
const remoteActions = fixture.actions ?? [];
const terminalInputModes = fixture.terminalInputModes ?? [];
const terminalControlCommands = fixture.terminalControlCommands ?? [];
const hosts = fixture.hosts ?? {};
const languageByExtension = fixture.languageByExtension ?? {};
const frameCrypto = fixture.frameCrypto ?? {};

if (!isPositiveInteger(protocolVersion)) {
	throw new Error(`protocolVersion must be a positive integer in ${fixturePath}`);
}

if (!isNonEmptyStringArray(remoteActions)) {
	throw new Error(`actions must be a non-empty string array in ${fixturePath}`);
}

if (!isNonEmptyStringArray(terminalInputModes)) {
	throw new Error(`terminalInputModes must be a non-empty string array in ${fixturePath}`);
}

if (!isNonEmptyStringArray(terminalControlCommands)) {
	throw new Error(`terminalControlCommands must be a non-empty string array in ${fixturePath}`);
}

for (const key of ['canonicalHost', 'releaseDownloadUrl', 'updateFeedUrl', 'canonicalRelayHost', 'defaultUserId']) {
	if (typeof hosts[key] !== 'string' || hosts[key].length === 0) {
		throw new Error(`Missing hosts.${key} in ${fixturePath}`);
	}
}

if (!Array.isArray(hosts.legacyRelayHosts) || !hosts.legacyRelayHosts.every(value => typeof value === 'string' && value.length > 0)) {
	throw new Error(`hosts.legacyRelayHosts must be a non-empty string array in ${fixturePath}`);
}

if (typeof hosts.relayHostPattern !== 'string' || hosts.relayHostPattern.length === 0) {
	throw new Error(`Missing hosts.relayHostPattern in ${fixturePath}`);
}

if (!Array.isArray(hosts.relayHostNormalizationCases) || !hosts.relayHostNormalizationCases.every(isRelayHostNormalizationCase)) {
	throw new Error(`hosts.relayHostNormalizationCases must be an array of { input, normalized } cases in ${fixturePath}`);
}

if (!Object.keys(languageByExtension).length || !Object.entries(languageByExtension).every(([key, value]) => key.length > 0 && typeof value === 'string' && value.length > 0)) {
	throw new Error(`languageByExtension must be a non-empty string map in ${fixturePath}`);
}

if (!isPositiveInteger(frameCrypto.nonceBytes) || !isPositiveInteger(frameCrypto.tagBytes) || !isPositiveInteger(frameCrypto.keyBytes)) {
	throw new Error(`frameCrypto nonceBytes, tagBytes, and keyBytes must be positive integers in ${fixturePath}`);
}

if (!Array.isArray(frameCrypto.base64UrlCases) || !frameCrypto.base64UrlCases.every(isBase64UrlCase)) {
	throw new Error(`frameCrypto.base64UrlCases must be an array of { bytes, encoded } cases in ${fixturePath}`);
}

