async function writeGeneratedFile(filePath, expectedContent) {
	let currentContent = undefined;
	try {
		currentContent = await readFile(filePath, 'utf8');
	} catch {
		// Missing generated outputs are handled below.
	}

	if (currentContent === expectedContent) {
		return;
	}

	if (checkOnly) {
		console.error(`${path.relative(root, filePath)} is out of date. Run npm run generate-vector-mobile-config.`);
		process.exitCode = 1;
		return;
	}

	await mkdir(path.dirname(filePath), { recursive: true });
	await writeFile(filePath, expectedContent);
	console.log(`Generated ${path.relative(root, filePath)}`);
}

function formatTsArray(values) {
	return `[\n${values.map(value => `\t${formatTsString(value)},`).join('\n')}\n]`;
}

function formatTsConstObject(values) {
	return `{\n${values.map(value => `\t${pascalIdentifier(value)}: ${formatTsString(value)},`).join('\n')}\n}`;
}

function formatTsObjectArray(values) {
	return `[\n${values.map(value => `\t${formatTsObject(value)},`).join('\n')}\n]`;
}

function formatTsTuples(entries) {
	return `[\n${entries.map(([key, value]) => `\t[${formatTsString(key)}, ${formatTsString(value)}],`).join('\n')}\n]`;
}

function formatTsObject(value) {
	const entries = Object.entries(value).map(([key, item]) => `${key}: ${formatTsValue(item)}`);
	return `{ ${entries.join(', ')} }`;
}

function formatTsValue(value) {
	if (typeof value === 'string') {
		return formatTsString(value);
	}
	if (value === null) {
		return 'null';
	}
	if (Array.isArray(value)) {
		return `[${value.map(formatTsValue).join(', ')}]`;
	}
	return String(value);
}

function formatTsString(value) {
	return `'${value
		.replace(/\\/g, '\\\\')
		.replace(/'/g, '\\\'')
		.replace(/\n/g, '\\n')
		.replace(/\r/g, '\\r')
		.replace(/\t/g, '\\t')}'`;
}

function formatSwiftString(value) {
	return `"${value
		.replace(/\\/g, '\\\\')
		.replace(/"/g, '\\"')
		.replace(/\n/g, '\\n')
		.replace(/\r/g, '\\r')
		.replace(/\t/g, '\\t')}"`;
}

function formatSwiftOptionalString(value) {
	return value === null ? 'nil' : formatSwiftString(value);
}

function formatSwiftEnum(name, values) {
	return `public enum ${name}: String, CaseIterable, Codable, Sendable {\n${values.map(value => `\tcase ${camelIdentifier(value)} = ${formatSwiftString(value)}`).join('\n')}\n}`;
}

function pascalIdentifier(value) {
	const identifier = value
		.split(/[^A-Za-z0-9]+/)
		.filter(Boolean)
		.map(part => part.charAt(0).toUpperCase() + part.slice(1))
		.join('');
	if (!identifier || /^[0-9]/.test(identifier)) {
		throw new Error(`Cannot derive a TypeScript identifier from ${formatTsString(value)}`);
	}
	return identifier;
}

function camelIdentifier(value) {
	const identifier = pascalIdentifier(value);
	return identifier.charAt(0).toLowerCase() + identifier.slice(1);
}

function isNonEmptyStringArray(value) {
	return Array.isArray(value) && value.length > 0 && value.every(item => typeof item === 'string' && item.length > 0);
}

function isRelayHostNormalizationCase(value) {
	return value
		&& typeof value === 'object'
		&& !Array.isArray(value)
		&& typeof value.input === 'string'
		&& (typeof value.normalized === 'string' || value.normalized === null);
}

function isBase64UrlCase(value) {
	return value
		&& typeof value === 'object'
		&& !Array.isArray(value)
		&& Array.isArray(value.bytes)
		&& value.bytes.every(byte => Number.isInteger(byte) && byte >= 0 && byte <= 255)
		&& typeof value.encoded === 'string';
}

function isPositiveInteger(value) {
	return Number.isInteger(value) && value > 0;
}
